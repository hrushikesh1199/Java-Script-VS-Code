 //Number Methods

 //1.toFixed()
    let num1 = 12.34567;
    console.log(num1.toFixed(2)); // Outputs: "12.35"

 //2.toPrecision()
    let num2 = 12.34567;
    console.log(num2.toPrecision(4)); // Outputs: "12.35"

 //3.toString()   
    let num3 = 255;
    console.log(num3.toString(16)); // Outputs: "ff"    

//4.valueOf()
    let num4 = new Number(100);
    console.log(num4.valueOf()); // Outputs: 100






//Math in JavaScript (Math Object)

    //1.Math.round()
    console.log(Math.round(4.7)); // Outputs: 5

    //2.Math.ceil()
    console.log(Math.ceil(4.2)); // Outputs: 5

    //3.Math.floor()
    console.log(Math.floor(4.7)); // Outputs: 4

    //4.Math.max()
    console.log(Math.max(10, 20, 5)); // Outputs: 20        

    //5.Math.min()
    console.log(Math.min(10, 20, 5)); // Outputs: 5

    //6.Math.random()
    console.log(Math.random()); // Outputs: A random number between 0 and 1

    //7.Math.sqrt()
    console.log(Math.sqrt(16)); // Outputs: 4

    //8.Math.pow()
    console.log(Math.pow(2, 3)); // Outputs: 8

    //9.Math.abs()
    console.log(Math.abs(-10)); // Outputs: 10

    //10.Math.sin()
    console.log(Math.sin(Math.PI / 2)); // Outputs: 1

    //11.Math.cos()
    console.log(Math.cos(0)); // Outputs: 1

    //12.Math.tan()
    console.log(Math.tan(Math.PI / 4)); // Outputs: 1

    //13.Math.log()
    console.log(Math.log(Math.E)); // Outputs: 1

    //14.Math.exp()
    console.log(Math.exp(1)); // Outputs: 2.718281828459045

    //15.Math.trunc()
    console.log(Math.trunc(4.9)); // Outputs: 4

    //16.Math.cbrt()
    console.log(Math.cbrt(27)); // Outputs: 3

    

