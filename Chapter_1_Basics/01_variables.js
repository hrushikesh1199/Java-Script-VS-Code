//Var
var name = "Hrushikesh";
console.log(name);



//Let
let age = 22;
console.log(age);   



//Const
const pi = 3.14;
console.log(pi);



//Variable inside a block
{
    let city = "Pune";
    console.log(city);
}



//const with objects
const person = {
    name: "Hrushikesh",
    age: 22
};
console.log(person);




//const with arrays
const numbers = [1, 2, 3, 4, 5];
console.log(numbers);   



//Undefined variable
let address;
console.log(address);



//Multiple variables
let pincode = 411033, country = "India";
console.log(pincode);
console.log(country);   

